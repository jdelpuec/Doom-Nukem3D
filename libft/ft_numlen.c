#include "includes/libft.h"

int	ft_numlen(int n)
{
	int a;

	a = 0;
	if (n == 0)
		a = 1;
	if (n < 0)
	{
		n = -n;
		a++;
	}
	while (n)
	{
		n = n / 10;
		a++;
	}
	return (a);
}
